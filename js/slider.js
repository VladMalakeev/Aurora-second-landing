$(document).ready(function() {
    console.log('ell = '+slidesArray[6]);
    $(window).scroll(function () {
        var center = $(window).height()/2;
        var scrollTop = $(window).scrollTop()+center;
        var scrollBottom = ($(window).height()+scrollTop)-center;
        var slider = $('#reviews_block').offset();
        var sliderTop = slider.top;
        var sliderBottom = $('#reviews_block').height()+sliderTop;

        if((scrollTop>sliderTop) && (scrollTop < sliderBottom)){
            console.log('start');
            $('.carousel-control-next').css({display:'flex'});
            $('.carousel-control-prev').css({display:'flex'});
         }
       else {
            $('.carousel-control-next').css({display:'none'});
            $('.carousel-control-prev').css({display:'none'});
        }
    });
    var slider = document.getElementById('carousel-inner');
    var indicators = document.getElementById('carousel-indicators');

   var width = $(window).width();

   if(width <= 1100){
        let count = slidesArray.length;
        for(let i = 0;i<count;i++){
            let indicator = document.createElement('li');
            indicator.setAttribute('data-target','#carouselExampleIndicators');
            indicator.setAttribute('data-slide-to',i);

            let item =document.createElement('div');
            if(i==0){
                indicator.className = 'active';
                item.className = 'carousel-item active';
            }else{
                item.className = 'carousel-item';
            }

            item.innerHTML = slidesArray[i];
            indicators.appendChild(indicator);
            slider.appendChild(item);
        }
    }
    else{
       let count = Math.ceil(slidesArray.length/2)
       for(let i = 0;i<count;i++){
           let indicator = document.createElement('li');
           indicator.setAttribute('data-target','#carouselExampleIndicators');
           indicator.setAttribute('data-slide-to',i);

           let elem_1 = document.createElement('div');
           let elem_2 = document.createElement('div');
           let item =document.createElement('div');
           let container =document.createElement('div');
           container.className = 'review_container';
           if(i==0){
               indicator.className = 'active';
               item.className = 'carousel-item active';
               elem_1.innerHTML = slidesArray[i];
               elem_2.innerHTML = slidesArray[i+1];
               container.appendChild(elem_1);
               container.appendChild(elem_2);
               item.appendChild(container);
           }else{
               item.className = 'carousel-item';
               elem_1.innerHTML = slidesArray[i+i];
               container.appendChild(elem_1);
               if(slidesArray[i+i+1] != undefined){
                   elem_2.innerHTML = slidesArray[i+i+1];
                   container.appendChild(elem_2);
               }
               item.appendChild(container);
           }

           indicators.appendChild(indicator);
           slider.appendChild(item);
       }
   }
});

